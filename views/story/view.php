            <div class="last_left">
                <p>如今她已年过80，却依然是各大品牌的宠儿。爱马仕邀她展示新品，Dior请她去助阵，她代言的劳力士广告也常常出现在《Vogue》、《Bazzar》等时尚杂志中。作为“工龄最长”的超模，与她合作的都是行业中最负盛名的摄影师——甚至连她老年证上的证件照，都出自英国著名时尚摄影师诺曼·帕金森之手。连《穿PARAD的女魔头》的导演，都要让梅丽尔·斯特里普染上一头银发来向她致敬。</p>
